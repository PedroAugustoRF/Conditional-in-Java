public class Condicional2ExpLogicas{
	public static void main(String[] args){
		// Expressões Lógicas quando avaliadas resulta Verdade ou falso
		// E = &&, Ou = ||, Não = !
		
		int x = 10, y = 10, z = 8;
		
		// se x e y são iguais, é verdade (x == y = true)
		// se x ou z é par, é verdade (x && y)
		// se y não for 8 é verdade !(y == 8) = true
		
		// && exige que todas condições sejam verdadeiras
		// || apenas uma condição precisa ser verdadeira
	}
}